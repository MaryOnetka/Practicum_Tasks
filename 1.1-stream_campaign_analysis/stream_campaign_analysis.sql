- 13. Задание — определить исполнителей, у которых есть более одного трека в одном жанре.
- Выведите уникальные пары исполнителя и жанра, где исполнитель имеет более одного трека в этом жанре.
- Результат отсортируйте сначала по названию жанра в лексикографическом порядке по возрастанию,
- затем в таком же порядке по имени исполнителя. Уберите все записи,
- в которых исполнитель неизвестен (в базе данных у таких артистов указан 'неизвестный исполнитель').

SELECT genre,
        artist
FROM songs
WHERE NOT artist='неизвестный исполнитель'
GROUP BY genre, artist
HAVING COUNT(track)>1
ORDER BY genre, artist;
