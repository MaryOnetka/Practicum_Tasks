- 6. Для маркетинговой кампании нужно отобрать жанры,
- у которых суммарная длительность прослушиваний треков превышает 1600 минут.
- В выгрузке округлите суммарную длительность прослушивания до двух знаков
- после точки и расположите данные по убыванию этого значения. Приведите поле duration к типу numeric.

SELECT genre,
        ROUND(SUM(duration::numeric)/60,2) AS sum_pr
FROM songs
GROUP BY genre
HAVING ROUND(SUM(duration::numeric)/60,2)>1600
ORDER BY sum_pr DESC;

- 7. Соберите статистику прослушиваний музыки по жанрам. Для каждого жанра нужно посчитать следующее:
- общее количество прослушиваний треков;
- суммарная длительность прослушиваний треков;
- минимальная длительность прослушивания треков;
- максимальная длительность прослушивания треков;
- средняя длительность прослушивания треков.
- Значения длительности оставьте в секундах. Поле со средней длительностью назовите average
- и значения в нём округлите до двух знаков после точки.
- Отсортируйте получившуюся таблицу в порядке убывания средней длительности.

SELECT genre,
        COUNT(duration) AS list_track,
        SUM(duration) AS all_dur,
        MIN(duration) AS min_dur,
        MAX(duration) AS max_dur,
        ROUND(AVG(duration::NUMERIC),2) AS average
FROM songs
GROUP BY genre
ORDER BY average DESC;

